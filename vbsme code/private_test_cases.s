# Preconditions:
#   1st parameter (a0) address of the first element of the dimension info (address of asize[0])
#   2nd parameter (a1) address of the first element of the frame array (address of frame[0][0])
#   3rd parameter (a2) address of the first element of the window array (address of window[0][0])
# Postconditions:	
#   result (v0) x coordinate of the block in the frame with the minimum SAD
#          (v1) y coordinate of the block in the frame with the minimum SAD


# Begin subroutine
vbsme:  
    li      $v0, 0              # reset $v0 and $V1
    li      $v1, 0

    # insert your code here
vars:
    lw $s0, 0($a0)          #rows of frame
    lw $s1, 4($a0)          #cols of frame
    lw $s2, 8($a0)          #rows of window
    lw $s3, 12($a0)         #cols of window
    sub $s4, $s0, $s2       #frameRows - windowRows
    sub $s5, $s1, $s3       #frameCols - windowCols


    addi $t0, $zero, 0      #i = 0
    addi $t1, $zero, 0      #j = 0
    addi $t4, $zero, -1     #changeX
    addi $t5, $zero, 1      #changeY
                            #Used t2 and t3 later for k and l
    addi $s0, $zero, 0      #s0 is a just was at the edge var - 0 if not, 1 if yes


mainloop:
    slt $t8, $s4, $t0           #next 4 lines are: i <= frows - wrows
    nor $t8, $t8, $zero             #or with 0, then flip the bit (will act as not)
    sll $t8, $t8, 31
    srl $t8, $t8, 31            #want it to be 1

    slt $t9, $s5, $t1           #                  j <= fcols - wcols
    nor $t9, $t9, $zero             #or with 0, then flip the bit (will act as not)
    sll $t9, $t9, 31
    srl $t9, $t9, 31            #want it to be 1

    and $t7, $t8, $t9            # i < frows - wrows && j < fcols - wcols
    beq $t7, $zero, exit        # exit if the result is 0, want it to be true
    add $s6, $zero, $zero       # sad = 0
    add $t2, $zero, $zero       #k = 0

outerfor:
    slt $t7, $t2, $s2           #k < wrows
    beq $t7, $zero, setmincondition     #exit the loop if that's false
    add $t3, $zero, $zero       #l = 0
    j innerfor

outerforadd:                    #after the inner for loop, add 1 to k
    addi $t2, $t2, 1            #and jump back to the outer loop
    j outerfor

innerfor:
    slt $t8, $t3, $s3                   # l < fcols
    beq $t8, $zero, outerforadd         # exit the loop if that's false
    #calc sad - using t6-t9, s6 for curent sad, 7 for minsad
    
    add $t6, $t0, $t2           #i + k
    add $t7, $t1, $t3           #j + 1
    mul $t6, $s1, $t6           # rows (t6) * wcols (zero based indexing so don't subtract)
    add $t6, $t7, $t6           # rows * cols + cols
    sll, $t6, $t6, 2            # multiply index for 1D array *4
    add $t6, $a1, $t6           # Add to base address
    lw $t8, 0($t6)              # Get frame[i+k, j+l]

    mul $t7, $s3, $t2           # k * fcols
    add $t7, $t7, $t3           # add l to k*fcols
    sll $t7, $t7, 2             # multiply index for 1D array *4
    add $t7, $a2, $t7           # Add base address
    lw $t9, 0($t7)              # Get window[k, l]

    sub $t7, $t8, $t9           #$t8-$t9 (frame[i+k, j+l] - window[k, l])
    slt $t6, $t7, $zero
    beq $t6, $zero, positive    # If positive, skip next line
    sub $t7, $zero, $t7         #subtract t7 from 0 to get -(t7)
    j positive                  #redundant but just to be sure

positive: #Continuation of innerfor
    add $s6, $t7, $s6           #Set $s6 to current sad
    j addinnerfor


addinnerfor:
    addi $t3, $t3, 1                #Add 1 to l
    j innerfor                      #Loop

setmin:
    add $s7, $s6, $zero             #Minsad = sad
    add $v0, $t0, $zero             #v0 (bestrow) = i
    add $v1, $t1, $zero             #v1 (bestcol) = j
    j endfors                       #Continue for loop


setmincondition:
    #Check if (i==0 and j ==0)
    slt $t7, $zero, $t0             # 0 < i
    slt $t8, $t0, $zero             # i < 0 - want both to be 0, not < || > than 0
    or $t7, $t7, $t8                # t9 is 0 if i == 0
    slt $t8, $zero, $t1             # 0 < j
    slt $t9, $t1, $zero             # j < 0
    or $t8, $t8, $t9                # need both to be 0
    or $t9, $t7, $t8                # need i == 0 (0) and j == 0 (0)
    beq $t9, $zero, setmin          # if $t9 == 0 set min

    #Check if sad <= minsad - won't ever trigger on first iteration
    slt $t9, $s7, $s6               #want it to be 0
    nor $t9, $t9, $zero             #or with 0, then flip the bit (will act as not)
    sll $t9, $t9, 31                #shift left to just get the last bit
    srl $t9, $t9, 31                #shift right to put 0s in upper 31 bits
    bne $t9, $zero, setmin          #branch if not 0 (0 from earlier will have been flipped)
    j endfors
    
endfors:
    #s6 (replaces sad) is j == 0, $t6 is j == fcols-wcols, t7 is i == frows-wrows, t8 is i == 0

    #Checks if we were just on the edge
     bne $s0, $zero, elseadd

     #Checks (i == 0 && j ==0)
     slt $t7, $zero, $t0            # 0 < i
     slt $t8, $t0, $zero            # i < 0 - want both to be 0, not < || > than 0
     or $t8, $t7, $t8               # t8 is 0 if i == 0
     slt $t7, $zero, $t1            # 0 < j
     slt $t9, $t1, $zero            # j < 0
     or $s6, $t7, $t9               # s6 is j == 0 - need both to be 0
     slt $t6, $t1, $s5              # j < fcols-wcols
     slt $t9, $s5, $t1              # j > fcols-wcols
     or $t6, $t6, $t9               # t6 is 0 if j == fcols - wcols 
     or $t9, $t8, $s6               # need i == 0 (0) and j == 0 (0)
	 or $t7, $t9, $t6				# t7 == 0 if i, j, and fcols-wcols == 0
	 beq $t7, $zero, addtoi			# if (i == 0 and j == 0) and j == fcols-wcols add to i instead
     beq $t9, $zero, addtoj         # if $t9 == 0 add to j

     #Checks (i == 0 && j == fcols-wcols)
     
     or $t9, $t6, $t8
     beq $t9, $zero, addtoi

     #Checks (j == 0 && i == frows-wrows)
     slt $t7, $t0, $s4
     slt $t9, $s4, $t0
     or $t7, $t7, $t9               #t7 is 0 if i == frows - wrows
     or $t9, $t7, $s6
     beq $t9, $zero, addtoj

     #Checks (i == frows-wrows && j == fcols-wcols)
     or $t9, $t6, $t7
     beq $t9, $zero, exit

     #Checks (i == 0)
     beq $t8, $zero, addtoj

     #Checks (j == fcols-wcols)
     beq $t6, $zero, addtoi

     #Checks (j == 0)
     beq $s6, $zero, addtoi

     #Checks (i == frows-wrows)
     beq $t7, $zero, addtoj
     j elseadd
    
  
elseadd: #Else - add changex to i and changey to j
    add $s0, $zero, $zero
    add $t0, $t0, $t4
    add $t1, $t1, $t5
    j mainloop
    
addtoj:
    addi $t6, $zero, -1           #set $t6 to -1
    addi $t1, $t1, 1            # add 1 to j
    mul $t4, $t4, $t6           #multiply changex by -1
    mul $t5, $t5, $t6           #multiply changex by -1
    addi $s0, $zero, 1
    j mainloop                  

addtoi:
    addi $t6, $zero, -1           #set $t6 to -1
    addi $t0, $t0, 1            # add 1 to i
    mul $t4, $t4, $t6           #multiply changex by -1
    mul $t5, $t5, $t6           #multiply changex by -1
    addi $s0, $zero, 1
    j mainloop                  


exit:
    jr $ra